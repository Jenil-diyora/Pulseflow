export const getThemeById = (id) => THEMES.find(t => t.id === id) || THEMES[0];

export const getThemeByScore = (score) => {
  // Filter themes that are meant to be unlocked by score (unlockScore > 0)
  const scoreBasedThemes = THEMES.filter(theme => theme.unlockScore > 0 && score >= theme.unlockScore);

  if (scoreBasedThemes.length === 0) return THEMES[0]; // Default to Ocean Depth

  // Return the highest reached theme based on score
  return scoreBasedThemes[scoreBasedThemes.length - 1];
};
